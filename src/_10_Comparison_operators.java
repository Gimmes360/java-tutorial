public class _10_Comparison_operators{
    public static void main (String[] args){
    //Comparison operators
        int davidAge = 18;
        int jennyAge = 20;
        System.out.println(davidAge > jennyAge);
        System.out.println(davidAge < jennyAge);
        System.out.println(davidAge == jennyAge);
        System.out.println(davidAge != jennyAge);
        System.out.println(davidAge >= jennyAge);
        System.out.println(davidAge <= jennyAge);

    }
}
