public class _8_Arithmetic_operations {
    public static void main (String[] args){
    //Arithmetic operations
        System.out.println(10 + 2);
        System.out.println(10 - 2);
        System.out.println(10 * 2);
        System.out.println(10 / 2);
        System.out.println(10 % 2);
        //Bodmas
        System.out.println(10 + 2 - (3 % 5) / 2 * 3 );
    }
}
